@ParametersAreNonnullByDefault
final class TcasTest {

  private def testTcas(name: String)
                          (pathCondition: Array[Double] => Boolean)
  : Option[Array[Double]] = {
    val solveRes: Option[Array[Double]] = TcasTest.cwTcasCases.find { pathCondition }
    if (solveRes.isDefined) {
      println(s"CW solve Tcas $name with : ${ solveRes.get.mkString(",") }")
    } else {
      println(s"CW can not solve Tcas $name.")
    }
    solveRes
  }

  @org.testng.annotations.BeforeClass
  def setUp(): Unit = {}

